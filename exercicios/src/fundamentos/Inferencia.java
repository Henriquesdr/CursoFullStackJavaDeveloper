package fundamentos;

public class Inferencia {

	public static void main(String[] args) {

		double a = 7.7;
		System.out.println(a);

		a = 7;
		System.out.println(a);

		var b = 7;
		System.out.println(b);

		var c = "Tipo string = Texto";
		System.out.println(c);

		// o tipo VAR indentifica o tipo da variavel que voce inicializa.
		// esse tipo inicializado nao pode ser alterado pra outro tipo.

	}
}
